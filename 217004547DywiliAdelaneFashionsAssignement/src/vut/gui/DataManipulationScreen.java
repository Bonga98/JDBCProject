    private void btnIncreaseGPPricesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncreaseGPPricesActionPerformed

        double perc;

        perc = Double.parseDouble(JOptionPane.showInputDialog("Enter Percentage To Increase By: "));
        if(perc >= 0 && perc <101){
            try {
                objPD.updatePrice(perc);
            } catch (NotFoundException e) {
                JOptionPane.showMessageDialog(null,"Could not update the percentage" +e.getMessage());
            }
        
        }else{
        JOptionPane.showMessageDialog(null, "Please enter a valid percentage");
        }
    }//GEN-LAST:event_btnIncreaseGPPricesActionPerformed

