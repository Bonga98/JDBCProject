    private void btnAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewActionPerformed
        try {
            if ((!txtCity.getText().isEmpty()) && (!txtSales.getText().isEmpty()) && (rdoCompany.isSelected())) {
                objPD = new AdelaneFashionsPD(cboStoreNumber.getSelectedItem().toString(), txtCity.getText(), AdelaneFashionsPD.Province.valueOf(cboProvinces.getSelectedItem().toString()), AdelaneFashionsPD.Ownership.C, Double.parseDouble(txtSales.getText()));

            } else if (rdoFranchise.isSelected()) {
                objPD = new AdelaneFashionsPD(cboStoreNumber.getSelectedItem().toString(), txtCity.getText(), AdelaneFashionsPD.Province.valueOf(cboProvinces.getSelectedItem().toString()), AdelaneFashionsPD.Ownership.F, Double.parseDouble(txtSales.getText()));
            }
            try {
                objPD.addStore(objPD);
                JOptionPane.showMessageDialog(null, "The entry was successfully added ");
            } catch (DuplicateException e) {
                JOptionPane.showMessageDialog(null, "Error!" + e.getMessage() + "Duplicate entry");
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "RE enter the customer data" + e.getMessage());
        }


    }//GEN-LAST:event_btnAddNewActionPerformed

